const os = require('os');

//Platform
console.log(os.platform());

//Cpu arch
console.log(os.arch());

//Cpu core info
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//Total memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());

//Uptime
console.log(os.uptime());