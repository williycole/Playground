package CompEncapPolymorph.Comp;

public class Ceiling {
    private int height;
    private int paintedColor;

    public Ceiling(int height, int paintColor) {
        this.height = height;
        this.paintedColor = paintColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
